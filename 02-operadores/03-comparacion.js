let a = 10;

// Operadores relacionales
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

// Operadores de igualdad
console.log(a == 10); // true
console.log(a != 10);
console.log(a == '10'); // true , debido a que compara si los valores son iguales y no si los tipos son iguales
console.log(a === '10'); // false, aqui se consulta si son iguales en valor y tipo
console.log(a !== '10'); // true
